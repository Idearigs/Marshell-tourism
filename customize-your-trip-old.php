        // Form enhancement script
        document.addEventListener('DOMContentLoaded', function() {
            // Mobile menu functionality
            const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
            const mobileMenu = document.querySelector('.mobile-menu');
            const closeButton = document.querySelector('.mobile-menu .close-button');

            function openMobileMenu() {
                if (mobileMenu) {
                    mobileMenu.classList.remove('tw--translate-x-full');
                    mobileMenu.style.transform = 'translateX(0)';
                }
            }

            function closeMobileMenu() {
                if (mobileMenu) {
                    mobileMenu.style.transform = 'translateX(-100%)';
                    setTimeout(() => {
                        mobileMenu.classList.add('tw--translate-x-full');
                    }, 300);
                }
            }

            if (mobileMenuBtn) {
                mobileMenuBtn.addEventListener('click', function() {
                    if (mobileMenu.style.transform === 'translateX(0px)') {
                        closeMobileMenu();
                    } else {
                        openMobileMenu();
                    }
                });
            }

            if (closeButton) {
                closeButton.addEventListener('click', closeMobileMenu);
            }

            // Smooth scrolling for anchor links
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });

            // Form submission
            const form = document.querySelector('form');
            if (form) {
                form.addEventListener('submit', function(e) {
                    e.preventDefault();

                    // Validate selections
                    if (selectedDestinations.length === 0) {
                        alert('Please select at least one destination for your trip.');
                        return;
                    }

                    if (!selectedVehicle) {
                        alert('Please select a vehicle for your trip.');
                        return;
                    }

                    // Get form data
                    const formData = new FormData(form);
                    const data = {
                        destinations: selectedDestinations,
                        vehicle: selectedVehicle
                    };
                    for (let [key, value] of formData.entries()) {
                        data[key] = value;
                    }

                    // Show success message
                    alert('Thank you for your submission! We will contact you within 24 hours to discuss your perfect wellness journey.');
                    console.log('Form data:', data);
                });
            }

            // Interest checkbox styling
            const checkboxes = document.querySelectorAll('.form-check');
            checkboxes.forEach(checkbox => {
                const input = checkbox.querySelector('input[type="checkbox"]');
                if (input) {
                    checkbox.addEventListener('click', function(e) {
                        if (e.target.type !== 'checkbox') {
                            input.checked = !input.checked;
                        }

                        if (input.checked) {
                            checkbox.style.borderColor = '#2c5aa0';
                            checkbox.style.backgroundColor = '#e3f2fd';
                        } else {
                            checkbox.style.borderColor = '#dee2e6';
                            checkbox.style.backgroundColor = 'white';
                        }
                    });
                }
            });

            // Focus styling for form controls
            const inputs = document.querySelectorAll('input, select, textarea');
            inputs.forEach(input => {
                input.addEventListener('focus', function() {
                    this.style.borderColor = '#2c5aa0';
                });

                input.addEventListener('blur', function() {
                    this.style.borderColor = '';
                });
            });
        });
