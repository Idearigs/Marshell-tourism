            // Passenger Counter Logic - Updated for new design
            const counterBtns = document.querySelectorAll('.counter-btn-new');
            const childrenInput = document.getElementById('children');
            const childrenAgesDiv = document.getElementById('children-ages');
            const childrenAgesContainer = document.getElementById('children-ages-container');

            console.log('Found counter buttons:', counterBtns.length);
            console.log('Children input:', childrenInput);
            console.log('Children ages div:', childrenAgesDiv);

            counterBtns.forEach(btn => {
                btn.addEventListener('click', function() {
                    const action = this.getAttribute('data-action');
                    const target = this.getAttribute('data-target');
                    const input = document.getElementById(target);
                    let currentValue = parseInt(input.value);
                    const min = parseInt(input.getAttribute('min'));
                    const max = parseInt(input.getAttribute('max'));

                    if (action === 'increase' && currentValue < max) {
                        input.value = currentValue + 1;
                    } else if (action === 'decrease' && currentValue > min) {
                        input.value = currentValue - 1;
                    }

                    // Handle children ages
                    if (target === 'children') {
                        updateChildrenAges();
                    }
                });
            });

            function updateChildrenAges() {
                const childrenCount = parseInt(childrenInput.value);

                if (childrenCount > 0) {
                    childrenAgesDiv.style.display = 'block';
                    childrenAgesContainer.innerHTML = '';

                    for (let i = 0; i < childrenCount; i++) {
                        const ageDiv = document.createElement('div');
                        ageDiv.className = 'child-age-item';
                        ageDiv.innerHTML = `
                            <label class="child-age-label">Child ${i + 1} Age</label>
                            <select name="child_age_${i + 1}" class="child-age-select" required>
                                <option value="">Select Age</option>
                                ${generateAgeOptions()}
                            </select>
                        `;
                        childrenAgesContainer.appendChild(ageDiv);
                    }
                } else {
                    childrenAgesDiv.style.display = 'none';
                }
            }

            function generateAgeOptions() {
                let options = '';
                for (let age = 2; age <= 17; age++) {
                    options += `<option value="${age}">${age} years</option>`;
                }
                return options;
            }
        });

        function toggleDestination(locationKey, pinElement) {
            const index = selectedDestinations.indexOf(locationKey);

            if (index > -1) {
                selectedDestinations.splice(index, 1);
                pinElement.classList.remove('selected');
            } else {
                selectedDestinations.push(locationKey);
                pinElement.classList.add('selected');
            }

            updateDestinationsList();
            updateProceedButton();
        }

        function updateDestinationsList() {
            const container = document.getElementById('selectedDestinations');

            if (selectedDestinations.length === 0) {
                container.innerHTML = '<p class="text-muted text-center py-5">Click on map locations to add them to your trip</p>';
                return;
            }

            let html = '';
            selectedDestinations.forEach((locKey, index) => {
                const loc = locations[locKey];
                html += `
                    <div class="rent-car-destination-card selected">
                        <div class="d-flex align-items-center">
                            <div class="destination-image-container">
                                <img src="${loc.image}" alt="${loc.name}" class="destination-image">
                                <div class="destination-number">${index + 1}</div>
                            </div>
                            <div class="destination-content">
                                <h6 class="destination-name">${loc.name}</h6>
                                <a href="#" class="read-more-link" onclick="return false;">
                                    <i class="ph ph-arrow-right me-1"></i>Read more
                                </a>
                            </div>
                        </div>
                    </div>
                `;
            });

            container.innerHTML = html;
        }

        function updateProceedButton() {
            const btn = document.getElementById('proceedToVehicles');
            btn.disabled = selectedDestinations.length === 0;
        }

