                    display: flex;
                    align-items: flex-start;
                    padding: 16px;
                    gap: 12px;
                }

                .alert-icon {
                    width: 24px;
                    height: 24px;
                    border-radius: 50%;
                    display: flex;
                    align-items: center;
                    justify-content: center;
                    color: white;
                    font-weight: bold;
                    font-size: 14px;
                    flex-shrink: 0;
                }

                .custom-alert-success .alert-icon {
                    background: #10b981;
                }

                .custom-alert-error .alert-icon {
                    background: #ef4444;
                }

                .alert-text {
                    flex: 1;
                }

                .alert-title {
                    font-weight: 600;
